public class Comparando {
    public static void main(String[] args) {

        int value1 = 1;
        int value2 = 2;
        if (value1 == value2) // igual a
            System.out.println("value1 == value2");
       if (value1 != value2) // diferente de
            System.out.println("value1 != value2");
        if (value1 > value2)  // maior que
            System.out.println("value1 > value2");
        if (value1 < value2)  // menor que
            System.out.println("value1 < value2");
        if (value1 <= value2)  // menor ou igual a
            System.out.println("value1 <= value2");
    }
}
