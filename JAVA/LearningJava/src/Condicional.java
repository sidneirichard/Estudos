public class Condicional {
    public static void main(String[] args) {
        int value1 = 1;
        int value2 = 2;
        if ((value1 == 1) && (value2 == 2))
            System.out.println("value1 é igual a 1 E value2 é igual a 2");
        if ((value1 != 1) || (value2 == 1))  // condiçao para 'printar' value1 diferente de 1 OU value2 ser igual a 1
            System.out.println("value1 é 1 OU value2 é 1");
    }
}


